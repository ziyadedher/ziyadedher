export default function Startup({
  onDone: handleDone,
}: {
  onDone: () => void;
}) {
  const [visibleLines, setVisibleLines] = useState<number>(0);

  useEffect(() => {
    let currentIndex = 0;
    let timeoutId: NodeJS.Timeout;

    const showNextLine = () => {
      if (currentIndex >= logLines.length) return;

      const [timestamp] = logLines[currentIndex];
      const nextDelay =
        currentIndex + 1 < logLines.length
          ? ((logLines[currentIndex + 1][0] - timestamp) * 1000) / 2
          : 0;

      setVisibleLines(currentIndex + 1);
      currentIndex++;
      window.scrollTo(0, document.body.scrollHeight);

      if (currentIndex < logLines.length) {
        timeoutId = setTimeout(showNextLine, nextDelay);
      }
    };

    showNextLine();

    const skipToEnd = () => {
      clearTimeout(timeoutId);
      setVisibleLines(logLines.length);
      setTimeout(() => {
        window.scrollTo(0, document.body.scrollHeight);
      }, 0);
    };

    window.addEventListener("keydown", skipToEnd);
    window.addEventListener("click", skipToEnd);
    window.addEventListener("touchstart", skipToEnd);

    return () => {
      clearTimeout(timeoutId);
      window.removeEventListener("keydown", skipToEnd);
      window.removeEventListener("click", skipToEnd);
      window.removeEventListener("touchstart", skipToEnd);
    };
  }, []);

  useEffect(() => {
    if (visibleLines >= logLines.length) {
      handleDone();
    }
  }, [visibleLines, handleDone]);

  return (
    <div className="text-xs">
      {logLines
        .slice(0, visibleLines)
        .map(([timestamp, subsystem, line], index) => (
          <div key={index}>
            <pre className="whitespace-pre-wrap">
              <span className="text-lime-500">{`[ ${timestamp.toFixed(6).padStart(12, " ")}]`}</span>
              <span className="text-yellow-200">{` ${subsystem ? subsystem + ": " : ""}`}</span>
              <span className="text-white">{line}</span>
            </pre>
          </div>
        ))}
      {visibleLines < logLines.length && <div className="h-4 invisible" />}
    </div>
  );
}
